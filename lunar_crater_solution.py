"""## Section 5: Remove Duplicate Detections with NMS

### Why Do We Have Duplicates?

Because our windows overlap by 20%, the same crater can appear in two or more windows.
The model may detect the same crater multiple times, giving us duplicate bounding boxes.

### What is IoU?

**Intersection over Union (IoU)** measures how much two bounding boxes overlap:

```
IoU = Area of Intersection / Area of Union
```

- IoU close to 1.0 means the two boxes are almost identical (one is a duplicate).
- IoU close to 0.0 means the two boxes barely overlap (they represent different craters).

### What is NMS?

**Non-Maximum Suppression (NMS)** removes duplicate boxes using IoU:
1. Sort all boxes by confidence score, highest first.
2. Keep the box with the highest score.
3. Remove any other box with IoU above 0.5 with the kept box (it is a duplicate).
4. Repeat with the remaining boxes.

A helper function `apply_nms()` is provided. You just need to call it and then build
a pandas DataFrame from the remaining detections.

**DataFrame columns you need to create:**
`x1, y1, x2, y2, confidence, center_x_px, center_y_px, width_px, height_px`

"""

# Helper function: Non-Maximum Suppression
# You do not need to change this function.

def apply_nms(boxes, scores, iou_threshold=0.5):
    '''
    Remove duplicate bounding boxes using Non-Maximum Suppression.

    Parameters:
        boxes         : list of [x1, y1, x2, y2] in pixel coordinates
        scores        : list of confidence scores, same length as boxes
        iou_threshold : boxes with IoU above this with a kept box are removed

    Returns:
        kept_boxes  : filtered list of [x1, y1, x2, y2]
        kept_scores : filtered list of confidence scores
    '''
    if len(boxes) == 0:
        return [], []

    boxes_t  = torch.tensor(boxes,  dtype=torch.float32)
    scores_t = torch.tensor(scores, dtype=torch.float32)

    keep = torch_nms(boxes_t, scores_t, iou_threshold)

    kept_boxes  = boxes_t[keep].numpy().tolist()
    kept_scores = scores_t[keep].numpy().tolist()

    return kept_boxes, kept_scores

