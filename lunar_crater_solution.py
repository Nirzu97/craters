"""## Section 10: Combined Safety Score

### Combining CDI and SWHI

CDI and SWHI use different units, so we cannot average them directly.
We first normalize each one to the range [0, 1]:

```
normalized_value = (value - min_value) / (max_value - min_value)
```

After normalization, 0.0 is the least hazardous cell and 1.0 is the most hazardous.
Then we average them:

```
safety_score = (cdi_normalized + swhi_normalized) / 2
```

A lower safety score means the cell is a safer landing candidate.

**Tasks:**
1. Write a `normalize()` function
2. Normalize `cdi_grid` and `swhi_grid`
3. Compute `safety_score` as their average
4. Identify safe cells: `safety_score <= SAFETY_THRESHOLD`
5. Find the top 3 safest cells (lowest safety score)

"""

# Normalize function
def normalize(grid):
    min_val = grid.min()
    max_val = grid.max()

    if max_val == min_val:
        return np.zeros_like(grid, dtype=float)

    return (grid - min_val) / (max_val - min_val)


# Quick test
test = np.array([[0.0, 5.0], [10.0, 20.0]])
result = normalize(test)
print("normalize([[0, 5], [10, 20]]) =")
print(result)
print("Expected: [[0.0, 0.25], [0.5, 1.0]]")

# Compute combined safety score
cdi_norm  = normalize(cdi_grid)
swhi_norm = normalize(swhi_grid)

safety_score = (cdi_norm + swhi_norm) / 2

print("Safety score grid (0 = safest, 1 = most hazardous):")
print(safety_score.round(3))

# Identify safe cells and find the top 3 safest locations
