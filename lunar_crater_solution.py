"""## Section 4: Sliding Window Inference

### Why Do We Need a Sliding Window?

The YOLOv8 model was trained on 640x640 pixel images. Our lunar image is much larger than that.
If we simply resized the whole image down to 640x640, we would lose resolution and small craters
would become invisible to the model.

Instead, we use a **sliding window**: we move a 640x640 crop across the full image, step by step,
running the model on each crop. Think of it like using a magnifying glass to scan a large map.

```
Full lunar image (e.g. 3000 x 2000 px)
+------------------------------------------+
| [W1]                                     |
|  640x640                                 |
|       [W2]                               |
|        640x640                           |
|              [W3] ...                    |
+------------------------------------------+
```

### How the Step Size Works

We do not want to miss craters that fall near the edge of a window.
So each window overlaps the previous one by a small amount (20% by default).

```
step = window_size * (1 - overlap)
     = 640 * (1 - 0.2)
     = 512 pixels
```

So window 1 starts at x=0, window 2 starts at x=512, window 3 at x=1024, and so on.
Each window overlaps its neighbor by 128 pixels.

### The Coordinate Offset

After detecting craters inside a window patch, the bounding box coordinates are relative
to the top-left corner of that patch. We need to convert them back to the full image coordinates.

If a window's top-left corner is at (x=512, y=0) in the full image, and the model detects
a crater at position (x1=100, y1=50) inside the window, then the crater's position in the
full image is:

```
full_image_x1 = 100 + 512 = 612
full_image_y1 = 50  + 0   = 50
```

In general: add the window's x offset to x1 and x2, and add the window's y offset to y1 and y2.

### What You Need to Do

A helper function `get_window_positions()` is provided below, it generates all the
(x, y) top-left corner positions. You need to:
1. Loop over all window positions
2. Crop each patch from the image
3. Run inference on the patch
4. Offset the detected boxes to global image coordinates
5. Collect all boxes and scores into lists

"""

# Helper function: compute all sliding window positions
# You do not need to change this function.

def get_window_positions(image_h, image_w, window_size, overlap):
    '''
    Returns a list of (x, y) top-left corner positions for sliding windows.

    The step between windows is: window_size * (1 - overlap).
    Extra windows are added at the right and bottom edges so the entire
    image is covered even if the image size is not divisible by the step.

    Parameters:
        image_h     : height of the full image in pixels
        image_w     : width of the full image in pixels
        window_size : size of each square window in pixels
        overlap     : fraction of window that overlaps with the previous one

    Returns:
        list of (x, y) tuples
    '''
    step = int(window_size * (1 - overlap))

    y_starts = list(range(0, image_h - window_size + 1, step))
    if not y_starts or y_starts[-1] + window_size < image_h:
        y_starts.append(max(0, image_h - window_size))

    x_starts = list(range(0, image_w - window_size + 1, step))
    if not x_starts or x_starts[-1] + window_size < image_w:
        x_starts.append(max(0, image_w - window_size))

    return [(x, y) for y in y_starts for x in x_starts]


