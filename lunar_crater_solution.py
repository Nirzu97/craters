"""## Section 8: Grid-Based Crater Density Index (CDI)

### The Idea

We divide the image into a `GRID_N x GRID_N` grid (10x10 = 100 cells).
For each cell we count craters and compute:

```
CDI = number of craters in cell / cell area in km squared
```

Higher CDI means more craters per km squared, which means more hazardous terrain.

### Finding Which Cell a Crater Belongs To

If the image has width `W` pixels and we have `GRID_N` columns,
each column covers `W / GRID_N` pixels wide. A crater at `center_x_px` belongs to:

```
col_index = int(center_x_px / cell_width_pixels)
```

Same logic for rows using `center_y_px` and `cell_height_pixels`.

Always clip to valid range: `min(col_index, GRID_N - 1)` to handle edge cases.

**Tasks:**
1. Compute `cell_h` and `cell_w` (pixels per cell)
2. Compute `cell_area_km2` (area of each cell in km squared)
3. Create a zero-filled `count_grid` of shape `(GRID_N, GRID_N)`
4. Loop over the DataFrame to fill in crater counts
5. Compute `cdi_grid = count_grid / cell_area_km2`
6. Display as a heatmap

"""

