"""## Section 9: Size-Weighted Hazard Index (SWHI)

### Why We Need a Second Metric

The CDI treats all craters equally regardless of their size.
But a single 2 km crater is far more dangerous than ten 50-meter craters.

The **Size-Weighted Hazard Index (SWHI)** fixes this by weighting each crater
by its diameter:

```
SWHI = sum of diameter_km for all craters in cell / cell area in km squared
```

A cell with large craters will have a high SWHI even if the raw count is low.

**Tasks:**
1. Create a zero-filled `swhi_raw` of shape `(GRID_N, GRID_N)`
2. Loop over the DataFrame and add `diameter_km` to the correct cell
3. Divide by `cell_area_km2` to get `swhi_grid`
4. Display as a heatmap

"""

swhi_raw = np.zeros((GRID_N, GRID_N))

for r_idx in range(GRID_N):
    for c_idx in range(GRID_N):
        # Calculate the pixel boundaries of the current grid cell
        cell_x1 = c_idx * cell_w
        cell_y1 = r_idx * cell_h
        cell_x2 = (c_idx + 1) * cell_w
        cell_y2 = (r_idx + 1) * cell_h

        # Iterate through all detected craters
        for _, row in df.iterrows():
            crater_x1, crater_y1, crater_x2, crater_y2 = row[['x1', 'y1', 'x2', 'y2']]

